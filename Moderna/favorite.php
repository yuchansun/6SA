window.onload = function () {
  const favorites = JSON.parse(localStorage.getItem('favorites')) || [];

  if (favorites.length === 0) {
    document.getElementById('favorite-list').innerHTML = "<p>尚未收藏任何學校。</p>";
    return;
  }

  const container = document.getElementById('favorite-list');
  
  favorites.forEach(schNum => {
    const schoolHTML = `
      <div class="favorite-item">
        <p>學校代碼：${schNum}</p>
        <button onclick="removeFavorite('${schNum}')">取消收藏</button>
      </div>
    `;
    container.innerHTML += schoolHTML;
  });
};

// ✅ 正確的刪除函式（只保留這一個）
function removeFavorite(schNum) {
  let favorites = JSON.parse(localStorage.getItem('favorites')) || [];
  favorites = favorites.filter(fav => fav !== schNum);
  localStorage.setItem('favorites', JSON.stringify(favorites));
  location.reload();
}
